    function rainbowText(text) {
        const colors = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'];
        let rainbowHTML = '';
        for (let i = 0; i < text.length; i++) {
            const color = colors[i % colors.length];
            rainbowHTML += `<span style="color: ${color}; display: inline-block; animation: rainbow 1s linear infinite;">${text[i]}</span>`;
        }
        return `<span style="display: inline-block;">${rainbowHTML}</span>`; // Для плавности анимации
    }

